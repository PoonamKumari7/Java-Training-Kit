package techment.Day4.Encapsulation;

public class Employee {
	
	private int id;
	private String name;
	
	//Initializing values through getter and setter 
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}	

}
